#!/usr/bin/env node
/**
 * Bake Cloudflare *build* env vars into the Worker bundle.
 * Build vars are not available at Worker runtime — this bridges that gap.
 *
 * Sources (first match wins):
 *   1. process.env.BUNNY_API_KEY / ADMIN_PASSWORD (Cloudflare Build variables)
 *   2. project .dev.vars (local)
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");
const outFile = path.join(root, "worker", "generated-secrets.ts");
const devVarsPath = path.join(root, ".dev.vars");

function loadDevVars() {
  const values = {};
  if (!fs.existsSync(devVarsPath)) return values;
  for (const line of fs.readFileSync(devVarsPath, "utf8").split("\n")) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith("#") || !trimmed.includes("=")) continue;
    const i = trimmed.indexOf("=");
    const key = trimmed.slice(0, i).trim();
    const value = trimmed.slice(i + 1).trim().replace(/^["']|["']$/g, "");
    values[key] = value;
  }
  return values;
}

const dev = loadDevVars();
const bunny = (process.env.BUNNY_API_KEY || dev.BUNNY_API_KEY || "").trim();
const admin = (
  process.env.ADMIN_PASSWORD ||
  dev.ADMIN_PASSWORD ||
  "7777"
).trim();

const contents = `/**
 * AUTO-GENERATED by scripts/inject-build-env.mjs — do not edit.
 * Populated from Cloudflare Build env vars or local .dev.vars.
 */
export const BUILD_SECRETS = {
  BUNNY_API_KEY: ${JSON.stringify(bunny)},
  ADMIN_PASSWORD: ${JSON.stringify(admin)},
} as const;
`;

fs.writeFileSync(outFile, contents);
const status = bunny
  ? `BUNNY_API_KEY set (${bunny.length} chars)`
  : "BUNNY_API_KEY missing — set Build variable BUNNY_API_KEY in Cloudflare";
console.log(`[inject-build-env] ${status}`);
